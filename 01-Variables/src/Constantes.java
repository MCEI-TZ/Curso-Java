public class Constantes {
    public static void main(String[] args) {
        //* Declaración de constantes
        //* Ejemplo del uso de constantes
        //! Nota para hacerla constante se pone un "final"
        final double PI = 3.1416;
        final String NOMBRE_AUTOR = "Miguel de Cervantes";
        final int ANIO_PUBLICACION = 1605;

        //* Impresión de constantes
        System.out.println("PI = " + PI);
        System.out.println("NOMBRE_AUTOR = " + NOMBRE_AUTOR);
        System.out.println("ANIO_PUBLICACION = " + ANIO_PUBLICACION);

    }
}
